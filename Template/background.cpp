background::background() {
	star1 = textFund::loadTexture("../Assets/PNG/Background/starBig.png", NULL);
	star2 = textFund::loadTexture("../Assets/PNG/Background/starSmall.png", NULL);
	lines = textFund::loadTexture("../Assets/PNG/Backgrounds/speedLine.png", NULL);
	nebula = textFund::loadTexture("../Assets/PNG/Backgrounds/nebula.png", NULL);
	backGround = textFund::loadTexture("../Assets/PNG/Backgrounds/starBackGround.png", NULL);
	
	loadMap(level1);

	

	src.x = src.y = 0;

}
void background::loadMap(int array[32][28])
{
	for (int row = 0; row < 25; row++) {
		for (int col = 0; col < 20; col++) {
			map[row][col] = array[row][col];
		}
	}
	
}

void background::drawMap()
{
	int type = 1;
	for (int row = 0; row < 32; row++) {
		for (int col = 0; col < 28; col++) {
			type = map [row][col];

			dst.x = col * 32;
			dst.y = row * 32;

			switch (type)
			{
			case 1:
				textFund::draw(star1, src, dst);
				std::cout << "Star Printed\n";
				break;
			case 2:
				textFund::draw(star2, src, dst);
				break;
			case 3:
				textFund::draw(nebula, src, dst);
				break;
			case 4:
				textFund::draw(lines, src, dst);
				break;
			}
			

		}
	}

}